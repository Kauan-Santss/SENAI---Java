class ComprimentoCircunferencia {
	public static void main (String [] args) {
		double circunferencia = 20;
		double raio = circunferencia / 2;
		double comprimentoCircunferencia = 2 * 3.14 * raio;
		System.out.println("Tamanho da circunferencia: " + circunferencia + "cm");
		System.out.println("Medida do raio: " + raio + "cm");
		System.out.println ("Comprimento da circunferencia: " + comprimentoCircunferencia + "cm");
	}
}